#include<bits/stdc++.h>
using namespace std;

    
int main()
{
	int i;
	char c;
	float f;
	double d;

	cout << "Size of char: " << sizeof(c) <<" bytes\n";
	cout << "Size of int: " << sizeof(i) <<" bytes\n";
	cout << "Size of float: " << sizeof(f) <<" bytes\n";
	cout << "Size of double: " << sizeof(d) <<" bytes\n";

    return 0;
  
}